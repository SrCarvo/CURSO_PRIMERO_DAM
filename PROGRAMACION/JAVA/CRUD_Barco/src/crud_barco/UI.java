        // TODO add your handling code here:
        /*Aqui estoy asignando valores a cada variable que se pide de los barcos*/
        /*Tambien estoy poniendo un par de condicionales; que los campos no esten vacios y que el esloran sea mayor o igual a 3*/
         Barco barcos2 = new Barco();
        
        String matricula = (matriculaInsertar.getText());
