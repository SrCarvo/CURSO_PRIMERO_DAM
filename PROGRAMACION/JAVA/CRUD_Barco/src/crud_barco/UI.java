    private void leerCompletoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leerCompletoActionPerformed
        // TODO add your handling code here:
        // Limpiar la tabla antes de agregar nuevos datos
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            FileInputStream fis = new FileInputStream("barcos.dat");
            ObjectInputStream ois = new ObjectInputStream(fis);

            List<Barco> barcos1 = gestor.obtenerTodosLosBarcos();
            for (Barco barco : barcos1) {
                model.addRow(new Object[]{barco.getMatrícula(), barco.getNombre(), barco.getTipo(), barco.getEslora(), barco.getAño()});
            }

            ois.close();
            fis.close();
        } catch (IOException e) {

        }
    }//GEN-LAST:event_leerCompletoActionPerformed

